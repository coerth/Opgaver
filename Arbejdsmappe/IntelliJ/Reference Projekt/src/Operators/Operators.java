//Matematiske operatore:
//
//Operator            Navn            Beskrivelse                             Eksempel
//+                   Plus            læg to værdier sammen                    a + b
//-                   Minus           træk en værdi fra en anden værdi         a - b
//*                   Gange           gang en værdi med en anden værdi         a * b
///                   Divider         divider en værdi med en anden værdi      a / b
//%                   Modulus         giver resten efter en dividering         a % b      (5 % 2 = 1 da 1 er tilbage)
//++                  Incrementer     læg en til værdien                       a++
//--                  Decrementer     træk en fra værdien                      a--
//
//Assignment operatore:
//
//        Operator            Navn            Beskrivelse                             Eksempel
//        =                   Lig med         sæt en værdi lig med noget andet        a = b (a har så samme værdi som b)
//        +=                  Plus oveni      læg en værdi oven i hvad den er         a += b (a har en værdi og så +b's værdi)'
//        -=                  Minus oveni     træk en værdi fra                       a -= b (b's' værdi bliver så trukket fra a)
//        *=                  Gange           gange en værdi med en anden             a *= b (gang a med b's' værdi)
//        /=                  Divider         divider en værdi med en anden           a /= b (divider a med b)
//        %=                  Modulus         lav modulus på en værdi med en anden    a %= b (divider a med b og giv resten)
//        der er noget flere jeg ikke forstår endnu.
//
//Comparision operatore:
//
//        Operator            Navn                      Beskrivelse                                 Eksempel
//        ==                  equal to                 en værdi er den samme som en anden             a == b
//        !=                  not equal to             en værdi er ikke den samme som en anden        a != b
//        >                   større end               en værdi er større end en anden                a > b
//        <                   mindre end               en værdi er mindre end en anden                a < b
//        >=                  større eller lig med    en værdi er større eller lig med                a >= b
//        <=                  mindre eller lig med    en værdi er mindre eller lig med                a <= b
//
//Logical operatore:
//        Operator            Navn            Beskrivelse                                           Eksempel
//        &&                  logisk and      giver true hvis begge ting er korrekte                a < 10 && b > 6
//        ||                  logisk or       giver true hvis en af dem er korrekte                 a < 10 || b > 6
//        !                   logisk not      giver omvendt resultat, false hvis det er korrekt     !(a < 10 && b > 6)