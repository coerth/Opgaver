//
//        der er åbenbart 2 slags casting:
//
//        Widening casting:
//        byte -> short -> char -> int -> long -> float -> double
//
//        Widening sker automatisk når man går fra en data type med som fylder mindre til en større:;
//
//        int intVariable = 23;
//        double doubleVariable = intVariable;
//
//        hvis de 2 variabler bliver printet vil det så således ud:
//        intVariable = 23;
//        doubleVariable = 23.0;
//
//
//        Narrowing casting:
//        double -> float -> long -> int -> char -> short -> byte
//
//        Narrowing skal ske manuelt, ved at putte den data type man vil have det skal være i parentes foran værdien.
//
//        double doubleVariable = 23.89;
//        int intVariable = (int) doubleVariable;
//
//        hvis de 2 variabler bliver printet vil det så således ud:
